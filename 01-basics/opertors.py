a = 5
b = 2

#arithmetic operators
print(a + b) #7
print(a - b) #3
print(a * b) #10
print(a / b) #2.5
print(a % b) #1

#relational operators
print(a == b) #False
print(a != b) #True
print(a > b) #True
print(a < b) #False

#assignment operators
num = 10
num += 10
print(num) #20

#logical operaotrs
print(not False) #True
print(not (a > b)) #False
